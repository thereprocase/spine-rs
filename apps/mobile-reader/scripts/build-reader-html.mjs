// Bundle epubjs + jszip + the reader bootstrap into a single self-contained
// HTML asset shipped under assets/reader.html. The RN reader screen loads this
// string into a WebView and posts the EPUB bytes over postMessage; the
// bootstrap reconstructs the ArrayBuffer and renders the book.
//
// Re-run this whenever epubjs is upgraded or the bootstrap changes:
//   pnpm build:reader

import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "..");
const workspaceRoot = resolve(root, "../..");

function loadDep(rel) {
  for (const base of [root, workspaceRoot]) {
    try {
      return readFileSync(resolve(base, "node_modules", rel), "utf8");
    } catch (_) {}
  }
  throw new Error(`could not locate ${rel} in package or workspace node_modules`);
}

const epubjs = loadDep("epubjs/dist/epub.min.js");
const jszip = loadDep("jszip/dist/jszip.min.js");
const bootstrap = readFileSync(resolve(__dirname, "reader-bootstrap.js"), "utf8");
const css = readFileSync(resolve(__dirname, "reader.css"), "utf8");

// Custom font @font-face rules are NOT inlined into reader.html — embedding
// 8 TTFs as base64 data URIs ballooned this asset to 25 MB and inflated the
// JS bundle accordingly. The reader's CSS font-family stacks (see
// src/reader/fonts.ts) declare the desired Spine families first and fall
// through to system serif/sans/mono. To make the custom families render,
// post a runtime "loadFont" message from the RN side with a base64-resolved
// asset for the active category — TODO, not wired yet.

const html = `<!doctype html>
<html lang="en">
<head>
<meta charset="utf-8" />
<meta name="viewport" content="width=device-width,initial-scale=1,maximum-scale=1,user-scalable=no,viewport-fit=cover" />
<title>Spine Reader</title>
<style>${css}</style>
</head>
<body>
<div id="viewer"></div>
<div id="status" aria-live="polite"></div>
<script>${jszip}</script>
<script>${epubjs}</script>
<script>${bootstrap}</script>
</body>
</html>
`;

const outDir = resolve(root, "assets");
mkdirSync(outDir, { recursive: true });
const out = resolve(outDir, "reader.html");
writeFileSync(out, html, "utf8");

// Also emit a TS module Metro can import directly so we don't have to register
// a custom asset extension.
const tsOutDir = resolve(root, "src/reader");
mkdirSync(tsOutDir, { recursive: true });
const b64 = Buffer.from(html, "utf8").toString("base64");
const ts =
  "// Generated by scripts/build-reader-html.mjs — do not edit.\n" +
  "// Re-run `pnpm build:reader` after changing the bootstrap or upgrading epubjs.\n" +
  `// Source size: ${(html.length / 1024).toFixed(1)} KB UTF-8.\n` +
  "export const READER_HTML_BASE64 =\n  " +
  JSON.stringify(b64) +
  ";\n";
writeFileSync(resolve(tsOutDir, "html.ts"), ts, "utf8");

console.log(
  `wrote ${out} (${(html.length / 1024).toFixed(1)} KB) + src/reader/html.ts (${(ts.length / 1024).toFixed(1)} KB)`,
);
